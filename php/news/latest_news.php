			Shea Homes is Named One Of 'America's Best Builders'
		</td>
		</tr>
		<tr>
		<td>
			<p><b>January 31, 2005 - BREA, CA</b>-Capturing one of the homebuilding industry's highest honors, Shea Homes has been named one of "America's Best Builders" for 2005.</p>

			<p>That accolade is awarded to companies that achieve excellence in every facet of operating and managing a homebuilding company, including finance/operations, design and construction, marketing, customer service, quality, and community and industry service. It is presented by the National Association of Home Builders and Builder Magazine, the leading publication in the housing industry, based on a comprehensive evaluation by a blue-ribbon panel of builders, home building consultants and past winners.</p>

			<p>This year's "America's Best Builders" were announced at an awards ceremony during the 2005 International Builders' Show at the Orlando World Center Marriott in Orlando, Florida.</p>

	
			<p>"Most builders excel in one discipline-be it customer service, great product design, construction know how, or financial planning," said Boyce Thompson, editorial director of BUILDER magazine, the leading magazine for the residential home building industry. "These companies manage to do it all. That takes incredible energy and focus."</p>

			<p>In accepting the award, Bert Selva, president and CEO of Shea Homes, commented, "In today's highly competitive new-home marketplace, this is a tremendous honor that is shared by all of our Shea Homes associates and trade partners. Most importantly, being one of 'America's Best Builders' means caring about our homebuyers, and when it comes to caring, we are committed to remaining the best for many years to come."</p>

			<p>In reporting Shea Homes' selection as one of "America's Best Builders," Builder Magazine applauded the company for establishing and adhering to its motto, "Caring Since 1881." The magazine noted that, "while doing the right thing has long been the undercurrent running through the company, the builder has given the ethos new life by making caring the central theme for dealing with customers and fellow employees."</p>

			<p>Selva elaborated, "Without our employees' strong belief in our culture of caring, the growth we have experienced simply would not have been possible."</p>

			<p>Builder Magazine added that Shea Homes "has maintained its attention to leadership principles and total quality management, enabling it to solidify its position as the nation's largest privately owned home builder."</p>

			<p>That position continues to strengthen. With the addition of new divisions, Shea Homes has grown rapidly amid ever-intensifying competition, from $1.6 billion in revenues in 1999 to nearly $2.8 billion in 2004. Today, the company has plans to add divisions in Sacramento, California, and Florida during 2005 and 2006, with an independent urban infill division likely further into the future. But Shea, comfortable outside Wall Street's spotlight, won't be fostering growth by joining the ranks of public builders. "There's absolutely no interest in the company going public," Selva said. "We love what we're able to accomplish."</p>

			<p>Shea Homes is a member of the Shea family of companies. Over the past century, members of the Shea family and their companies have accomplished many extraordinary milestones, including completing work on the foundation of the landmark Golden Gate Bridge, working on San Francisco's Bay Area Rapid Transit System (BART) and serving as a managing partner for the construction of the Hoover Dam. Yet Shea Homes' most satisfying achievements continue to revolve around listening to its customers' needs and satisfying those needs by providing an extensive selection of new home floor plans and quality workmanship in the finest neighborhoods and communities available today.</p>

			<p>Shea Homes is currently building throughout California, Arizona, Colorado, Washington, and North and South Carolina. Additional information is available by visiting the company's website at sheahomes.com.</p>

